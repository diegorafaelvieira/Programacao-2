package usuariosmaquina;

public class AbreMaquina {
	private String nome;
	private String hora;
	
	public AbreMaquina(String nome, String hora) {
		super();
		this.nome = nome;
		this.hora = hora;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getHora() {
		return hora;
	}

	public void setHora(String hora) {
		this.hora = hora;
	}
	
	
	
	
}
