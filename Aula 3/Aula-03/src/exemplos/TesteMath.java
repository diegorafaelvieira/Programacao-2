package exemplos;

public class TesteMath {

	public static void main(String[] args) {
		//raiz quadrada de 900
		System.out.println(Math.sqrt(900));
		
		// raiz quadrada de 9
		double x = Math.sqrt(9);
		System.out.println(x);
		
		// 2 elevado ao cubo
		double y = Math.pow(2,3); 
		System.out.println(y);
		
		// Retorna o valor maior
		double i = Math.max(2,5);
		System.out.println(i);
	}

}
